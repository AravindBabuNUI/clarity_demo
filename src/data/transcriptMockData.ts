// Helper function to get status (cycles through statuses)
const statuses: ("approved" | "pending" | "rejected" | "review")[] = [
  "approved",
  "pending",
  "rejected",
  "review",
];
let statusIndex = 0;
const getStatus = (): "approved" | "pending" | "rejected" | "review" => {
  const status = statuses[statusIndex % statuses.length];
  statusIndex++;
  return status;
};

